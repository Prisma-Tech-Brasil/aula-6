// -*-*-*-*-* Mais sobre o console.log() -*-*-*-*-*-

// Se executa assim, mas precisa passar um parâmetro nela:
console.log();

// O parâmetro pode ser uma mensagem (string):
console.log("O console.log executou");

// Pode-se passar uma variável:
const idade = 24;
console.log(idade);


// O console é importante para entender o que está acontecendo no seu código,
// e o console.log nos ajuda nisso
